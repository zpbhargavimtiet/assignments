package aLLassignments;

public class Staticmethod {
	//Program on Static method//

	static void div()
	{
		int a=10, b=2;
		int result = a/b;
		System.out.println(result);
	}
	static void modulo()
	{
		int a=10, b=2;
		int result = a%b;
		System.out.println(result);
	}
	public static void main(String[] args) {
		div();	
		modulo();
	}

}
