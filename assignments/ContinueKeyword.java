package aLLassignments;

public class ContinueKeyword {

	public static void main(String[] args) {
//Write a program using 'continue' statement to skip printing any numbers 
//		from 100 to 200	
	
	for (int i=100; i<=200;i++)	
	{
		if(i>=110)
		{
			continue;
		}
	System.out.println(i);
	}
		
		
	}

}
