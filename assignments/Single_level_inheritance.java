package aLLassignments;
class login //parenet class
{
	static void name()
	{
		System.out.println("padma");
	}
}
public class Single_level_inheritance extends login //child class
{
	static void mail()
	{
		System.out.println("bhargavi@gmail.com");
	}
public static void main(String[] args) 
{

	name();
	mail();
}

}
//The above is single level-inheritance//acquiring the property from 
//parent class..
